            $('#yourPhoneErrorMsg').text('');
            $('#yourEmailErrorMsg').text('');
            $('#yourCompanyErrorMsg').text('');
            $('#yourMessageErrorMsg').text('');

            $('#successMsg').fadeIn('fast').delay(3000).fadeOut('fast');
            setTimeout(function() {
              $( ".close" ).trigger( "click" );
            }, 1000);

          }

        },
        error: function(response)
        {
